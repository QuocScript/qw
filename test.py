input_str = int(input('hãy nhập số vào đây để tôi kiểm tra chẵn lẻ: '))

if input_str.isdigit():
    num = int(input_str)
    if num % 2 == 0:
        print("Số bạn nhập là số chẵn")
    else:
        print("Số bạn nhập là số lẻ")
else:
    print("Đây không phải là mộ`t số nguyên.")

